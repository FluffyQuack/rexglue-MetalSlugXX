#include "upgrade_prompt.h"

#include <algorithm>
#include <cctype>
#include <cstdio>
#include <iostream>
#include <string>

#include <fmt/format.h>

#ifdef _WIN32
#include <io.h>
#define rexglue_isatty(fd) _isatty(fd)
#define rexglue_fileno(stream) _fileno(stream)
#else
#include <unistd.h>
#define rexglue_isatty(fd) isatty(fd)
#define rexglue_fileno(stream) fileno(stream)
#endif

namespace rexglue::cli {

namespace {

void PrintPlan(const std::vector<OverwriteEntry>& plan, std::string_view from_version,
               std::string_view to_version) {
  std::cerr << fmt::format("ReXGlue: project last generated by SDK {}; current SDK is {}\n\n",
                           from_version.empty() ? "(unstamped)" : from_version, to_version);
  std::cerr << "The following file(s) will be overwritten as part of the upgrade:\n";
  for (const auto& e : plan) {
    std::cerr << "  - " << e.path.generic_string() << "\n";
  }
  std::cerr << "\n";
}

bool StdinIsTty() {
  return rexglue_isatty(rexglue_fileno(stdin)) != 0;
}

bool ReadConsent() {
  std::cerr << "Continue? [y/N] " << std::flush;
  std::string line;
  if (!std::getline(std::cin, line))
    return false;
  std::transform(line.begin(), line.end(), line.begin(),
                 [](unsigned char c) { return std::tolower(c); });
  auto first = line.find_first_not_of(" \t\r\n");
  if (first == std::string::npos)
    return false;
  auto last = line.find_last_not_of(" \t\r\n");
  std::string trimmed = line.substr(first, last - first + 1);
  return trimmed == "y" || trimmed == "yes";
}

}  // namespace

rex::Result<UpgradeConsent> PromptForUpgradeConsent(const std::vector<OverwriteEntry>& plan,
                                                    std::string_view from_version,
                                                    std::string_view to_version, bool force) {
  if (force) {
    return rex::Ok(UpgradeConsent::Granted);
  }

  PrintPlan(plan, from_version, to_version);

  if (!StdinIsTty()) {
    return rex::Err<UpgradeConsent>(
        rex::ErrorCategory::Config,
        fmt::format("Upgrade required for this project ({} -> {}). Re-run with --force to consent.",
                    from_version.empty() ? "(unstamped)" : from_version, to_version));
  }

  return rex::Ok(ReadConsent() ? UpgradeConsent::Granted : UpgradeConsent::Declined);
}

}  // namespace rexglue::cli
